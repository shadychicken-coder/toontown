<h1>Journal of the Whills</h1>
<h3>By Unknown</h3>
<p>
The Old Republic was the Republic of legend, greater than distance or time. No need to note where it was or
whence it came, only to know that ... it <i>the</i> Republic.
</br></br>
Once, under the wise rule of the Senate and the protection of Jedi Knights, the Republic
throve and grew. But as often happens when wealth and power pass beyond the admirable
and attain the awesome, then appear those evil ones who have greed to match.
</br></br>
So it was the Republic at its height. Like the greatest of trees, able to withstand any external attack, the Repbulic rotted from within though the danger was not visible from outside.
</br></br>
Aided and abetted by restless, power-hungry individuals within the government, and the massive organs of commerce, the ambitious Senator Palpatine caused himself to be elected
President of the Republic. He promised to reunite the disaffected among the people and to restore the remembered glory of the Republic. Once secure in office he declared himself Emperor,
shutting himself away from the populace. Soon he was controlled by the very assistants and boot-lickers he had appointed to high office, and the cries of the poeple for justice did not reach his ears.
</br></br>
Having exteriminated through treachery and deception the Jedi Knights, guardians of justice in the galaxy, the Imperial governors and bureacrats prepared to institute a reign of terror among the disheartened
worlds of the galaxy. Many used the Imperial forces and the name of the increasingly isolated Emperor to further their own personal ambitions.
</br></br>
But a small number of systems rebelled at these new outrages. Declaring themselves opposed to the New Order they began the great battle to restore the Old Republic.
</br></br>
From the beginning they were vastly outnumbered by the systems held in thrall by the Emperor. In those first dark days it seemed certain the bright flame of resistance would be extinguished before it could cast the light of new truth across a galaxy of opressed and beaten
peoples ...
